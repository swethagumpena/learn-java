public class Primitives {
    public static void main(String[] args) {
        int rollNo = 64; // 4 bytes
        // primitive datatype: datatype that you can't break further
        // String is not primitive.
        // Can break string into individual characters [String: double quotes]
        char letter = 's'; // char: single quotes.
        float marks = 99.7f; // 4 bytes
        // all decimal values are by default of type double
        double largeDecimalNumbers = 838383773.3883; // 8 bytes
        long largeInteger = 383737737373773L; // 8 bytes
        boolean check = false;
    }
}
